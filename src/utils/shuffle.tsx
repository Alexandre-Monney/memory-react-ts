// The Fisher-Yates algorithm, trouvé sur internet, pour mélanger mon tableau de carte aléatoirement

const shuffleArray = (array: any[]): any[] => {
  for (let i = array.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    const temp = array[i];
    array[i] = array[j];
    array[j] = temp;
  }
  return array;
};

export default shuffleArray;
